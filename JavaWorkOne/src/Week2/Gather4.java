package Week2;

public class Gather4 {
	public int Gather(int number1,int number2) {
		return number1+number2;
	}
	public int Gather(int number1,int number2,int number3) {
		return number1+number2+number3;
	}
	public int interest(int number1,int number2) {
		return number1-number2;
	}
	public int multiply(int number1,int number2) {
		return number1*number2;
	}
	public int plenty(int number1,int number2) {
		return number1/number2;
	}
}
